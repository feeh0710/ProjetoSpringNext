package br.projetoparticularnext.com.dto;

public class ClienteEndereco {

	private String cpf;
	private String nome;
	private String rg;
	private String email;
	private String senha;
	private String tipo;
	private String cidade;
	private String estado;
	private String bairro;
	private String numero;
	private String logradouro;
	private String cep;
	private String telefone;
	
	
	
	public String getTelefone() {
		return telefone;
	}
	public void setTelefone(String telefone) {
		this.telefone = telefone;
	}
	public String getCpf() {
		return cpf;
	}
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getRg() {
		return rg;
	}
	public void setRg(String rg) {
		this.rg = rg;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getSenha() {
		return senha;
	}
	public void setSenha(String senha) {
		this.senha = senha;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getCidade() {
		return cidade;
	}
	public void setCidade(String cidade) {
		this.cidade = cidade;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getBairro() {
		return bairro;
	}
	public void setBairro(String bairro) {
		this.bairro = bairro;
	}
	public String getNumero() {
		return numero;
	}
	public void setNumero(String numero) {
		this.numero = numero;
	}
	public String getLogradouro() {
		return logradouro;
	}
	public void setLogradouro(String logradoro) {
		this.logradouro = logradoro;
	}
	public String getCep() {
		return cep;
	}
	public void setCep(String cep) {
		this.cep = cep;
	}
	@Override
	public String toString() {
		return "ClienteEndereco [cpf=" + cpf + ", nome=" + nome + ", rg=" + rg + ", email=" + email + ", senha=" + senha
				+ ", tipo=" + tipo + ", cidade=" + cidade + ", estado=" + estado + ", bairro=" + bairro + ", numero="
				+ numero + ", logradoro=" + logradouro + ", cep=" + cep + "]";
	}
	
	
	

}
